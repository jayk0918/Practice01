package com.javaex.practice;

public class Ex05 {
	
	public static void main(String[] args) {
		
		//double 형 speed, time distance 변수 선언
		double speed, time, distance;
		
		//speed = 90.0, time = 60.0
		speed = 90.0;
		time = 60.0;
		
		//speed*time = distance
		distance = speed*time;
		
		//출력
		System.out.println(distance);
		
	}

}
